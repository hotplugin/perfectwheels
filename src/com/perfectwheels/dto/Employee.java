package com.perfectwheels.dto;

import java.io.Serializable;

/**
 *
 * @author ShresthBirodh
 */
public class Employee implements Serializable { /* but you aren't using any of the serializable functionality here */


    int id;
    String name;
    String address;
    String phone;
    String department;
    String joinedDate;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }

    public String getJoinedDate() {
        return joinedDate;
    }

    public void setJoinedDate(String joinedDate) {
        this.joinedDate = joinedDate;
    }

    public String toString() {

        return "Name: " + name + " Address " + address + " Phone: " + phone + " Department " + department;

    }
}
